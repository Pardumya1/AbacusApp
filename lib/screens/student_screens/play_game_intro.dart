                                image: new AssetImage("images/bg_orange.png"),
                                fit: BoxFit.cover)
                        ),
                        child: data()
                      ),
                      onTap: () => Navigator.push(
                        context,
                        MaterialPageRoute(
                          builder: (context) => GameLevel(),
                        ),
                      )
                    );
                  },
                ),

              )

            ],
          )
      ),
    );

  }

  right_image(){
